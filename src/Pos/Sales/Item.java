package Pos.Sales;

public class Item {
	private String name;
	private int each;
	private int price;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getEach() {
		return each;
	}
	public void setEach(int each) {
		this.each = each;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
}
